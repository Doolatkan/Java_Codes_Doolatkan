package day8_ifElseIfElseIfElseStatements;

public class task39_TrafficLight {

	public static void main(String[] args) {

         char light = 'g';
        		 
         switch(light) {
         
         case 'R':
         case 'r':
        	 System.out.println("Red");
        	 break;
         case 'O':
         case 'o':
        	 System.out.println("Orange");
        	 break;
         case 'G':
         case 'g':
        	 System.out.println("Green");
        	 break;
         }

	}

}
