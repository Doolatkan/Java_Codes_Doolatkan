package review_session.week7;

public class String_Replace {

	public static void main(String[] args) {


		String amazonRsult = "iphone case (212,713 Results)";
		String numbers = "0123456789";
		String newStr = "";
		for(int i=0;i<amazonRsult.length();i++) {
			if(numbers.contains(amazonRsult.substring(i,i+1))){
				newStr = newStr + amazonRsult.substring(i,i+1);
			}
			
		}
		System.out.println(newStr);

	}

}
